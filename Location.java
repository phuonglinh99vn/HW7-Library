package HW7;

public class Location {
	private int id;
	private String bookshelf;
	
	public Location(int id, String bookshelf) {
		super();
		this.id = id;
		this.bookshelf = bookshelf;
	}
	
	public int getId() {
		return id;
	}

	public String getbookshelf() {
		return bookshelf;
	}
}
